void binstr2int(int text[4][4], string str) {
	unsigned char* output = new unsigned char[16];
	for (int i = 0; i <= 15; i++) {
		int start = i * 8;
		int temp = 0;
		for (int j = start; j <= start + 7; j++) {
			int each = 1;
			for (int s = 1; s <= 7 - j + start; s++) {
				each *= 2;
			}
			if (str[i] == '1') {
				temp += each;
			}
		}
		output[i] = temp;
	}
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			text[j][i] = output[j * 4 + i];
		}
	}
}

void KeyExpansion(int key[4][4], int w[11][4][4])
{
	for (int i = 0; i < 4; ++i)
	{
		for (int j = 0; j < 4; j++)
		{
			w[0][i][j] = key[j][i];

		}
	}
	for (int i = 1; i < 11; ++i)
	{
		for (int j = 0; j < 4; ++j)
		{
			int temp[4];
			if (j == 0)
			{
				temp[0] = w[i - 1][3][1];
				temp[1] = w[i - 1][3][2];
				temp[2] = w[i - 1][3][3];
				temp[3] = w[i - 1][3][0];
				for (int k = 0; k < 4; ++k)
				{
					int m = temp[k];
					int row = m / 16;
					int col = m % 16;
					temp[k] = S[row][col];
					if (k == 0)
					{
						temp[k] = temp[k] ^ rC[i - 1];
					}
				}
			}
			else
			{
				temp[0] = w[i][j - 1][0];
				temp[1] = w[i][j - 1][1];
				temp[2] = w[i][j - 1][2];
				temp[3] = w[i][j - 1][3];
			}
			for (int x = 0; x < 4; x++)
			{
				w[i][j][x] = w[i - 1][j][x] ^ temp[x];
			}
		}
	}
}

string int2binstr(int text[4][4]) {
	string result;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			string str = "00000000";
			int temp = text[j][i];
			for (int k = 7; k >= 0; k--) {
				str[k] = '0' + temp % 2;
				temp /= 2;
			}
			result += str;
		}
	}
	return result;
}

//基本运算
int mul(int a, int b)
{
	int third = b & 0x8;
	int second = b & 0x4;
	int first = b & 0x2;
	int firstMod = b % 2;
	int res = 0;

	if (third)
	{
		int temp = a;
		for (int i = 1; i <= 3; ++i)
		{
			temp = temp << 1;
			if (temp >= 256)
			{
				temp = temp ^ 0x11b;
			}
		}
		temp = temp % 256;
		res = res ^ temp;
	}
	if (second)
	{
		int temp = a;
		for (int i = 1; i <= 2; ++i)
		{
			temp = temp << 1;
			if (temp >= 256)
			{
				temp = temp ^ 0x11b;
			}
		}
		temp = temp % 256;
		res = res ^ temp;
	}
	if (first)
	{
		int temp = a;
		temp = temp << 1;
		if (temp >= 256)
		{
			temp = temp ^ 0x11b;
		}
		temp = temp % 256;
		res = res ^ temp;
	}
	if (firstMod)
	{
		res = res ^ a;
	}
	return res;
}

void ByteSub(int in[4][4], int type)
{
	for (int i = 0; i < 4; i++)
	{
		for (int j = 0; j < 4; j++)
		{
			int temp = in[i][j];
			int row = temp / 16;
			int col = temp % 16;
			if (type == 1)
			{
				in[i][j] = S[row][col];
			}
			if (type == 0)
			{
				in[i][j] = rS[row][col];
			}
		}
	}
}

void ShiftRow(int in[4][4], int type) {
	for (int i = 0; i < 4; i++)
	{
		for (int j = 0; j < i; j++)
		{
			if (type == 1)
			{
				int temp = in[i][0];
				in[i][0] = in[i][1];
				in[i][1] = in[i][2];
				in[i][2] = in[i][3];
				in[i][3] = temp;
			}
			else
			{
				int temp = in[i][3];
				in[i][3] = in[i][2];
				in[i][2] = in[i][1];
				in[i][1] = in[i][0];
				in[i][0] = temp;
			}
		}
	}
}

void MixColumn(int in[4][4], int type)
{
	for (int i = 0; i < 4; i++)
	{
		int t0 = in[0][i];
		int t1 = in[1][i];
		int t2 = in[2][i];

		int t3 = in[3][i];
		if (type == 1)
		{
			in[0][i] = mul(t0, 2) ^ mul(t1, 3) ^ t2 ^ t3;
			in[1][i] = t0 ^ mul(t1, 2) ^ mul(t2, 3) ^ t3;
			in[2][i] = t0 ^ t1 ^ mul(t2, 2) ^ mul(t3, 3);
			in[3][i] = mul(t0, 3) ^ t1 ^ t2 ^ mul(t3, 2);
		}
		else
		{
			in[0][i] = mul(t0, 14) ^ mul(t1, 11) ^ mul(t2, 13) ^ mul(t3, 9);
			in[1][i] = mul(t0, 9) ^ mul(t1, 14) ^ mul(t2, 11) ^ mul(t3, 13);
			in[2][i] = mul(t0, 13) ^ mul(t1, 9) ^ mul(t2, 14) ^ mul(t3, 11);
			in[3][i] = mul(t0, 11) ^ mul(t1, 13) ^ mul(t2, 9) ^ mul(t3, 14);
		}
	}
}

void AddRoundKey(int in[4][4], int key[4][4])
{
	for (int i = 0; i < 4; ++i)
	{
		for (int j = 0; j < 4; j++)
		{
			in[i][j] = in[i][j] ^ key[j][i];
		}
	}
}

//实现
void Encode(int in[4][4], int key[4][4])
{
	int type = 1;
	int subKey[11][4][4];
	KeyExpansion(key, subKey);
	AddRoundKey(in, subKey[0]);
	for (int i = 1; i <= 10; ++i)
	{
		ByteSub(in, type);
		ShiftRow(in, type);
		if (i != 10)
		{
			MixColumn(in, type);
		}
		AddRoundKey(in, subKey[i]);
	}
}

void Decode(int in[4][4], int key[4][4])
{
	int type = 0;
	int subKey[11][4][4];
	KeyExpansion(key, subKey);
	AddRoundKey(in, subKey[10]);
	for (int i = 9; i >= 0; --i)
	{
		ShiftRow(in, type);
		ByteSub(in, type);
		AddRoundKey(in, subKey[i]);
		if (i != 0)
		{
			MixColumn(in, type);
		}

	}
}
